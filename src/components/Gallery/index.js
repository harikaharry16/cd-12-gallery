class Gallery extends Component {
  state = {
    ourImage: imagesList[0].imageUrl,
    ourImageText: imagesList[0].imageAltText,
    ourImageId: imagesList[0].id,
  }

  onChangeState = id => {
    this.setState({
      ourImage: imagesList[id].imageUrl,
      ourImageText: imagesList[id].imageAltText,
      ourImageId: id,
    })
  }

  render() {
    const {ourImage, ourImageText, ourImageId} = this.state
    return (
      <div className="bg-container">
        <img src={ourImage} alt={ourImageText} className="image-pic" />
        <h1 className="head">Nature Photography</h1>
        <p className="para">Nature Photography by Rahul</p>
        <ul className="img-list">
          {imagesList.map(each => (
            <ThumbnailItem
              key={each.id}
              eachImage={each}
              onChangeState={this.onChangeState}
              isActive={each.id === ourImageId}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
